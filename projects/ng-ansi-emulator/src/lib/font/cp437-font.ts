/**
 * CP437 8x16 bitmap font - full canonical glyph set.
 *
 * 256 glyphs * 16 bytes = 4096 bytes total. Each byte is one pixel row,
 * MSB = leftmost pixel. Hand-encoded to match the IBM VGA ROM font for
 * every box-drawing, block, shading, Latin-extended, and Greek/math
 * character that BBS ANSI art relies on.
 *
 * Default fallback for any glyph not explicitly authored is a SOLID FILLED
 * BLOCK (0xFF on every row), not a hollow rectangle.
 */

const FONT_BYTES = 4096;
const GLYPH_HEIGHT = 16;
const GLYPH_WIDTH = 8;

let fontData: Uint8Array = new Uint8Array(FONT_BYTES);

export const CP437_GLYPH_HEIGHT = GLYPH_HEIGHT;
export const CP437_GLYPH_WIDTH = GLYPH_WIDTH;

/** Replace the active font (for swapping in a canonical VGA ROM dump). */
export function setFontData(bytes: Uint8Array): void {
  if (bytes.length !== FONT_BYTES) {
    throw new Error(`CP437 font must be exactly ${FONT_BYTES} bytes; got ${bytes.length}`);
  }
  fontData = new Uint8Array(bytes);
}

export function getFontData(): Uint8Array {
  return fontData;
}

export function getGlyph(codePoint: number): Uint8Array {
  const cp = codePoint & 0xff;
  const offset = cp * GLYPH_HEIGHT;
  return fontData.subarray(offset, offset + GLYPH_HEIGHT);
}

