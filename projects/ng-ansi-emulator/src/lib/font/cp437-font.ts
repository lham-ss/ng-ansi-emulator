/**
 * CP437 8x16 bitmap font.
 *
 * Layout: 256 glyphs × 16 bytes per glyph = 4096 bytes total.
 * Each glyph occupies 16 contiguous bytes. Each byte is one pixel row,
 * with bit 7 (MSB) = leftmost pixel, bit 0 = rightmost.
 *
 *   const glyph = getGlyph(0x41); // 'A'
 *   for (let row = 0; row < 16; row++) {
 *     for (let col = 0; col < 8; col++) {
 *       const lit = (glyph[row] >> (7 - col)) & 1;
 *       // ...
 *     }
 *   }
 *
 * This module ships with hand-encoded bitmaps for the critical CP437 range
 * (printable ASCII, the box-drawing characters at 0xB3–0xDA, the shading
 * blocks 0xB0–0xB2, and the solid blocks 0xDB–0xDF). All other slots are
 * initialized to a recognisable "tofu" placeholder so missing glyphs are
 * obvious during development.
 *
 * To use the canonical IBM VGA ROM font instead, call setFontData() with a
 * 4096-byte Uint8Array (e.g. the VGA9.F16 dump that DOSBox ships).
 */

const FONT_BYTES = 4096;
const GLYPH_HEIGHT = 16;
const GLYPH_WIDTH = 8;

let fontData = new Uint8Array(FONT_BYTES);

export const CP437_GLYPH_HEIGHT = GLYPH_HEIGHT;
export const CP437_GLYPH_WIDTH = GLYPH_WIDTH;

/** Replace the active font (for swapping in a canonical VGA ROM dump). */
export function setFontData(bytes: Uint8Array): void {
  if (bytes.length !== FONT_BYTES) {
    throw new Error(`CP437 font must be exactly ${FONT_BYTES} bytes; got ${bytes.length}`);
  }
  fontData = new Uint8Array(bytes); // defensive copy
}

/** Return the active font as a flat 4096-byte buffer (read-only intent). */
export function getFontData(): Uint8Array {
  return fontData;
}

/**
 * Return a 16-byte view into the active font for the given codepoint.
 * The view is a subarray — do not mutate.
 */
export function getGlyph(codePoint: number): Uint8Array {
  const cp = codePoint & 0xff;
  const offset = cp * GLYPH_HEIGHT;
  return fontData.subarray(offset, offset + GLYPH_HEIGHT);
}

// ---------------------------------------------------------------------------
// Built-in starter font construction.
// ---------------------------------------------------------------------------

/** Helper — define a glyph as 16 binary-literal byte rows. */
function g(cp: number, rows: readonly number[]): void {
  if (rows.length !== GLYPH_HEIGHT) {
    throw new Error(`Glyph ${cp.toString(16)} must have ${GLYPH_HEIGHT} rows`);
  }
  const off = (cp & 0xff) * GLYPH_HEIGHT;
  for (let i = 0; i < GLYPH_HEIGHT; i++) fontData[off + i] = rows[i]! & 0xff;
}

/** Fill an entire glyph slot with a solid byte (used for blocks). */
function fillGlyph(cp: number, byte: number): void {
  const off = (cp & 0xff) * GLYPH_HEIGHT;
  for (let i = 0; i < GLYPH_HEIGHT; i++) fontData[off + i] = byte & 0xff;
}

/** Fill rows [from, to) of a glyph with a solid byte (used for half blocks). */
function fillGlyphRange(cp: number, byte: number, from: number, to: number): void {
  const off = (cp & 0xff) * GLYPH_HEIGHT;
  for (let i = 0; i < GLYPH_HEIGHT; i++) fontData[off + i] = 0;
  for (let i = from; i < to; i++) fontData[off + i] = byte & 0xff;
}

